function normalizeSearch(value: string) {
  return value.trim().toLowerCase();
}

export function getWilayas() {
  return [...WILAYAS].sort((a, b) => a.code.localeCompare(b.code));
}

export function getCommunes(wilayaCode: string) {
  const communes = COMMUNES[wilayaCode] ?? [];
  return [...communes].sort((a, b) => a.localeCompare(b, "ar"));
}

export function searchWilayas(query: string) {
  const normalized = normalizeSearch(query);
  if (!normalized) return getWilayas();

  return getWilayas().filter((wilaya) => {
    return wilaya.name_ar.includes(query) || wilaya.name_fr.toLowerCase().includes(normalized);
  });
}

export function getWilayaByCode(code: string) {
  return WILAYAS.find((wilaya) => wilaya.code === code) ?? null;
}

export function getZoneSlugForCommune(wilayaCode: string, commune: string, indexOverride?: number) {
  const index = indexOverride ?? (COMMUNES[wilayaCode]?.indexOf(commune) ?? 0);
  const position = String(index + 1).padStart(2, "0");
  return `w${wilayaCode}-${position}`;
}
